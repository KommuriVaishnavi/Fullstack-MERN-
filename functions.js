// functions
//  1)using function keyword
function hello(){
    // console.log('hello from function');
    return 2+2;
}
console.log(hello());
// 2) variable expression
const some= function hello(){
    return 2+2;
}
console.log(some());
// 3) anonymous expression
const x=function(){
    return 2+2;
}
console.log(x());

// control statements
// if-else
let i=40;
if (i>0){
    console.log('number is positive');
}
else{
    console.log('number is negative');
}
// --------------------------------------
// AND(&&) operator and OR(||) operator
let num1=2;
let num2=3;
if (num1>1 && num2>4){
    console.log('statement is true');
}
else{
    console.log('statement is false');
}
// ---------------------
if (num1>1 || num2>4){
    console.log('statement is true');
}
else{
    console.log('statement is false');
}
//  FOR loop
// repeating a block of code in specified times
for (let i=0;i<=5;i++){
    console.log('iteration '+i);
}